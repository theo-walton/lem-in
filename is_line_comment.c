#include "lem.h"

int	is_line_comment(char *str)
{
	int i;

	i = 1;
	if (str[0] == '\0')
		return (0);
	if (str[0] != '#')
		return (0);
	if (str[1] == '#')
		return (0);
	return (1);
}
