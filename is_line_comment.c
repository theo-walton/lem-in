int	is_line_comment(char *str)
{
	int i;

	i = 1;
	if (str[0] == '\0')
		return (0);
	if (str[0] != '#')
		return (0);
	while (str[i] != '\0' && str[i] != '\n')
	{
		if (str[i] == '#')
			return (0);
		i++;
	}
	return (1);
}
